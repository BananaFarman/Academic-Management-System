
package Utilities;

/**
 *
 * @author Sebas
 */
public class Action {
    private String name; 
    /**
     * 
     */
    public Action(){}
    /**
     * 
     * @param name 
     */
    public Action(String name){
        this.name = name;
    }
    /**
     * 
     * @return 
     */
    public String getName() {
        return name;
    }
    /**
     * 
     * @param name 
     */
    public void setName(String name) {
        this.name = name;
    }
    
    
}
